<script setup>
import { reactive, ref, onMounted } from "vue";
const buttonMode = ref(null);
const form = ref(null);

const initialData = () => ({
	store: "",
	name: "",
	phone: "",
	consumption: null,
	payment: "",
});
const data = reactive(initialData());

const storeInput = ref(null);
const nameInput = ref(null);
const phoneInput = ref(null);
const consumptionInput = ref(null);
const payment = ref(null);

const inputs = reactive([
	storeInput,
	nameInput,
	phoneInput,
	consumptionInput,
	payment,
]);
onMounted(() => {
	setTimeout(() => {
		inputs.forEach((input) => {
			input.value.addEventListener("blur", () =>
				toggleInValidClass(input.value)
			);
			input.value.nextElementSibling.addEventListener("click", () =>
				input.value.reportValidity()
			);
		});
	}, 2000);
});

function submitHandler(e) {
	const allValid = inputs.every((input) => input.value.reportValidity());
	inputs.forEach((input) => {
		toggleInValidClass(input.value);
		if (input.value.parentElement.classList.contains("invalid")) {
			addShakeAnimation(input.value.parentElement);
		}
	});
	buttonMode.value = false;
	if (!allValid) return;
	buttonMode.value = true;

	// After blur reset Form to initial
	e.target.addEventListener("blur", () =>
		setTimeout(() => {
			Object.assign(data, initialData());
			inputs.forEach((input) =>
				input.value.parentElement.classList.remove("valid")
			);
			buttonMode.value = null;
		}, 1000)
	);
}

function toggleInValidClass(input) {
	input.parentElement.classList.toggle("invalid", !input.checkValidity());
	input.parentElement.classList.toggle("valid", input.checkValidity());
}
function addShakeAnimation(target) {
	target.classList.add("shake");
	target.addEventListener("animationend", () =>
		target.classList.remove("shake")
	);
}
</script>

